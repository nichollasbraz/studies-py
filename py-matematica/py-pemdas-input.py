num = int(input("Digite um número: "))
ptc = num * num
dbl = num * 2
div_hlf = num / 2
div_int = num // 2
mod = num % 2
add = num + 5
sub = num - 5

print("\nValor original: {}".format(num))
print("Potência: {}".format(ptc))
print("Dobro: {}".format(dbl))
print("Divisão por dois: {}".format(div_hlf))
print("Divisão inteira por dois: {}".format(div_int))
print("Módulo: {}".format(mod))
print("Adicionando 5: {}".format(add))
print("Subtraindo 5: {}".format(sub))
